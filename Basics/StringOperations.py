# Basic String Operations

hello = "Hello"

print(hello)
print(hello.capitalize())
print(hello.casefold())
print(hello.isalnum())
print(hello.isalpha())
print(hello.isdecimal())
print(hello.isdigit())
print(hello.isidentifier())
print(hello.replace('e', 'a'))
print('Hello' == hello)
print("Some,csv,values".split(","))
