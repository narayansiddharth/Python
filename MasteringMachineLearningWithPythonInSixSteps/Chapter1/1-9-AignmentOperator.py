# Listing 1-9. Example code for assignment operators
# Variable x holds 10 and variable y holds 5
x = 5
y = 10
x += y
print("Value of a post x+=y is ", x)
x *= y
print("Value of a post x*=y is ", x)
x /= y
print("Value of a post x/=y is ", x)
x %= y
print("Value of a post x%=y is ", x)
x **= y
print("Value of x post x**=y is ", x)
x //= y
print("Value of a post x//=y is ", x)
